export const BAEGEUM_SKIN_PRESETS = baegeumSkinSpecs.map(createBaegeumPreset);

const legacyPresets = DRAWING_WORLD_PRESETS
  .filter((preset) => preset.id !== "painter")
  .map((preset) => withDirectionShell(preset));

export const SKIN_PRESETS = [...BAEGEUM_SKIN_PRESETS, ...legacyPresets];

export function normalizeSkinPresetId(presetId) {
  const id = String(presetId || "").trim();
  if (!id || id === "painter") return DEFAULT_SKIN_PRESET_ID;
  return SKIN_PRESETS.some((preset) => preset.id === id) ? id : DEFAULT_SKIN_PRESET_ID;
}

export function findSkinPreset(presetId) {
  const id = normalizeSkinPresetId(presetId);
  return SKIN_PRESETS.find((preset) => preset.id === id) || SKIN_PRESETS[0];
}

export function resolveSkinPresetDirection(presetId, direction = "side") {
  const preset = findSkinPreset(presetId);
  return preset.directions?.[direction] ? direction : preset.defaultDirection || "side";
}

export function drawPresetToContext(ctx, presetOrId, direction = "side") {
  const preset = typeof presetOrId === "string" ? findSkinPreset(presetOrId) : withDirectionShell(presetOrId);
  const resolvedDirection = preset.directions?.[direction] ? direction : preset.defaultDirection || "side";
  const draw = preset.directions?.[resolvedDirection] || preset.draw || clearSkinContext;
  draw(ctx);
}

function createBaegeumPreset(spec) {
  const draw = (ctx) => drawSkinSpec(ctx, spec);
  return {
    id: spec.id,
    name: spec.name,
    draw,
    defaultDirection: "side",
    directions: { side: draw, front: null, back: null }
  };
}

function withDirectionShell(preset) {
  if (preset?.directions) return preset;
  const draw = preset?.draw || clearSkinContext;
  return { ...preset, draw, defaultDirection: "side", directions: { side: draw, front: null, back: null } };
}

function drawSkinSpec(ctx, spec) {
  clearSkinContext(ctx);
  for (const [color, x, y, width, height] of spec.rects) {
    ctx.fillStyle = color;
    ctx.fillRect(x, y, width, height);
  }
}
